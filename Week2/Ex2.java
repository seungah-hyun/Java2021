package Week2;

public class Ex2 {

	public static void main(String[] args) {
	      System.out.println("Byte size"+ Byte.BYTES);
	      System.out.println("Byte Min"+ Byte.MIN_VALUE);
	      System.out.println("Byte Max"+ Byte.MAX_VALUE);
	      System.out.println();
	      System.out.println("int size"+Integer.BYTES);
	      System.out.println("int Min"+ Integer.MIN_VALUE);
	      System.out.println("int Max"+Integer.MAX_VALUE);
	      System.out.println();
	      System.out.println("char size"+ Character.BYTES);
	      System.out.println("char Min"+ (long)Character.MIN_VALUE);
	      System.out.println("char Max"+ (long)Character.MAX_VALUE);
	      System.out.println();
	      System.out.println("double size"+ Double.BYTES);
	      System.out.println("double Min"+ (double)Double.MIN_VALUE);
	      System.out.println("double Max"+ (double)Double.MAX_VALUE);
	      
	      

	}

}
